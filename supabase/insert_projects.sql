2. 选品广场选品（不要自己压货）
3. 挂载商品链接
4. 引导用户下单

**佣金比例**：
- 食品、日用品：10-20%
- 美妆、服饰：20-40%
- 知识类产品：30-50%

### 路径2：接广告（月入3000-20000）

**开通条件**：
- 1000-10000粉丝
- 稳定的播放量

**接单渠道**：
1. 官方平台（星图、星广）
2. 第三方平台（群响、快接单）
3. 主动BD品牌方

**报价参考**：
- 1万粉：100-300元/条
- 10万粉：500-2000元/条
- 50万粉：3000-10000元/条

### 路径3：直播带货（月入5000-50000）

**开通条件**：
- 抖音：实名认证即可
- 快手：实名认证+缴纳保证金

**直播带货流程**：
1. 选品（选高佣金、高性价比）
2. 预热视频（提前1-3天）
3. 直播脚本准备
4. 直播中引导下单
5. 复盘优化

### 路径4：知识付费（月入10000+）

